def split_round_robin(items, k):
    buckets = [[] for _ in range(k)]
    for i, it in enumerate(items):
        buckets[i % k].append(it)
    return buckets

def launcher_main(args):
    input_root = Path(args.input_root)
    out_root   = Path(args.output_root)
    ensure_dir(out_root)

    files = list_input_files(input_root)
    if not files:
        raise RuntimeError(f"No input files found under: {input_root}")

    gpu_ids = [int(x) for x in args.gpus.split(",") if x.strip() != ""]
    if not gpu_ids:
        raise RuntimeError("No GPUs specified. Use --gpus 0,1,2,3")

    log(f"Files found: {len(files):,}")
    log(f"GPUs: {gpu_ids}")

    if args.limit_files:
        files = files[:args.limit_files]
        log(f"LIMIT_FILES enabled: processing only first {len(files)} file(s)")

    shards = split_round_robin(files, len(gpu_ids))

    import subprocess
    procs = []
    for local_rank, gpu_id in enumerate(gpu_ids):
        shard_file = out_root / f"_shard_gpu{gpu_id}.txt"
        with open(shard_file, "w") as f:
            for p in shards[local_rank]:
                f.write(str(p) + "\n")

        cmd = [
            "python", str(Path(__file__).name),
            "--mode", "worker",
            "--gpu-id", str(gpu_id),
            "--shard-file", str(shard_file),
            "--input-root", str(input_root),
            "--output-root", str(out_root),
            "--text-col", args.text_col,
            "--id-col", args.id_col,
            "--batch-size", str(args.batch_size),
            "--chunk-rows", str(args.chunk_rows),
            "--seed", str(args.seed),
            "--omp-threads", str(args.omp_threads),
        ]
        if args.max_rows:
            cmd += ["--max-rows", str(args.max_rows)]

        log(f"Launching GPU {gpu_id}: {' '.join(cmd)}")
        procs.append(subprocess.Popen(cmd))

    exit_codes = [p.wait() for p in procs]
    if any(code != 0 for code in exit_codes):
        raise SystemExit(f"One or more workers failed: {exit_codes}")

    log("✅ BLOCK 2 COMPLETED (all GPU workers finished successfully)")

def worker_main(args):
    shard_paths = []
    with open(args.shard_file, "r") as f:
        for line in f:
            s = line.strip()
            if s:
                shard_paths.append(Path(s))

    if not shard_paths:
        log(f"[GPU {args.gpu_id}] No files in shard. Exit.")
        return

    log(f"[GPU {args.gpu_id}] Shard files: {len(shard_paths):,}")

    out_root = Path(args.output_root)

    for in_file in shard_paths:
        embed_one_file(
            in_file=in_file,
            out_root=out_root,
            text_col=args.text_col,
            id_col=args.id_col,
            batch_size=args.batch_size,
            chunk_rows=args.chunk_rows,
            max_rows=args.max_rows,
            gpu_id=args.gpu_id,
            seed=args.seed,
            omp_threads=args.omp_threads,
        )

    log(f"[GPU {args.gpu_id}] Worker finished.")


