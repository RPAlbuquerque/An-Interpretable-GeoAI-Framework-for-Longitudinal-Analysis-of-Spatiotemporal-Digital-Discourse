def build_argparser():
    ap = argparse.ArgumentParser(description="BLOCK 2 — Multi-GPU Embeddings (Ultra-fast, Resumable)")
    ap.add_argument("--mode", choices=["launcher", "worker"], default="launcher")

    ap.add_argument("--input-root",  default="tweets_brazil/final_corpus", type=str)
    ap.add_argument("--output-root", default="block_2_embeddings", type=str)

    ap.add_argument("--text-col", default="text", type=str)
    ap.add_argument("--id-col",   default="message_id", type=str)

    ap.add_argument("--gpus", default="0,1,2,3", type=str)
    ap.add_argument("--gpu-id", default=0, type=int)
    ap.add_argument("--shard-file", default="", type=str)

    ap.add_argument("--batch-size", default=256, type=int)
    ap.add_argument("--chunk-rows", default=200_000, type=int)
    ap.add_argument("--max-rows", default=0, type=int)

    ap.add_argument("--seed", default=42, type=int)
    ap.add_argument("--limit-files", default=0, type=int)

    # Key speed control: keep this low-ish per GPU worker to avoid fighting for CPUs
    ap.add_argument("--omp-threads", default=8, type=int)

    return ap

def main():
    args = build_argparser().parse_args()
    if args.mode == "launcher":
        launcher_main(args)
    else:
        if not args.shard_file:
            raise RuntimeError("--shard-file is required in worker mode")
        worker_main(args)

if __name__ == "__main__":
    main()