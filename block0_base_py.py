#!/usr/bin/env python3
# ============================================================
# BLOCK 0 — BRAZIL FILTER (HPC SAFE / RESUME / FAIL-SOFT)
#
# - Filters GeoTweets to Brazil (bbox + date)
# - Keeps ALL original columns
# - Chunked (memory-safe)
# - Skips malformed files (logs & continues)
# - Resume via DONE markers
# - Progress bar in terminal (tqdm)
