def log(msg: str):
    ts = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    print(f"[{ts}] {msg}", flush=True)

def safe_write_text(path: Path, text: str):
    tmp = path.with_suffix(path.suffix + ".tmp")
    tmp.write_text(text)
    tmp.replace(path)

def done_marker_for(infile: Path) -> Path:
    # unique stable id: filename only is fine because folder is flat in semantic_filtered
    return DONE_DIR / f"{infile.name}.done.json"

def outputs_for(infile: Path, chunk_id: int):
    # One input file may be written in multiple chunks
    base = infile.name.replace(".csv.gz", "")
    emb_path  = OUT_EMB  / f"{base}.chunk{chunk_id:05d}.npz"
    meta_path = OUT_META / f"{base}.chunk{chunk_id:05d}.csv.gz"
    return emb_path, meta_path

# =============================================================================
# GPU WORKER
# =============================================================================

def encode_texts(embedder: SentenceTransformer, texts, batch_size: int):
    """
    Robust GPU encoder with automatic batch shrinking on OOM.
    """
    bs = batch_size
    while True:
        try:
            with torch.inference_mode():
                emb = embedder.encode(
                    texts,
                    batch_size=bs,
                    show_progress_bar=False,
                    convert_to_numpy=True,
                    normalize_embeddings=True
                )
            return emb, bs
        except RuntimeError as e:
            if "out of memory" in str(e).lower():
                torch.cuda.empty_cache()
                bs = max(64, bs // 2)
                if bs == 64:
                    raise
                continue
            raise

def worker_main(gpu_id: int, file_list: list):
    """
    One process bound to one GPU.
    """
    os.environ["CUDA_VISIBLE_DEVICES"] = str(gpu_id)
    device = "cuda" if torch.cuda.is_available() else "cpu"

    log(f"🧠 [GPU {gpu_id}] Loading model on device={device} | threads={CPU_THREADS}")
    embedder = SentenceTransformer(EMBED_MODEL_NAME, device=device)
    embedder.max_seq_length = MAX_SEQ_LEN
    log(f"✅ [GPU {gpu_id}] Model loaded: {EMBED_MODEL_NAME}")

    kept_files = 0
    kept_rows  = 0

    for idx, infile in enumerate(file_list):
        dm = done_marker_for(infile)
        if dm.exists():
            continue

        t0 = time.time()
        chunk_id = 0
        rows_total_infile = 0

        try:
            # Stream within file to keep memory stable
            reader = pd.read_csv(
                infile,
                compression="gzip",
                dtype=str,
                chunksize=MAX_ROWS_PER_CHUNK,
                low_memory=False
            )

            for df in reader:
                if df.empty:
                    continue
                if "message_id" not in df.columns or "text" not in df.columns:
                    continue

                df["text"] = df["text"].astype(str)
                # Drop NA texts only (rows), preserve columns in original dataset by not rewriting it
                df = df[df["text"].notna()]
                if df.empty:
                    continue

                texts = df["text"].tolist()
                ids   = df["message_id"].astype(str).tolist()

                # Encode (GPU)
                emb, used_bs = encode_texts(embedder, texts, DEFAULT_BATCH_SIZE)

                # Convert dtype
                emb = emb.astype(EMB_DTYPE, copy=False)

                # Output paths
                emb_path, meta_path = outputs_for(infile, chunk_id)

                # Write embeddings shard
                # Store ids as numpy array of strings (object); ok for npz
                np.savez_compressed(
                    emb_path,
                    message_id=np.array(ids, dtype=object),
                    embeddings=emb
                )

                # Write metadata shard (csv.gz) for joins / audits
                meta = pd.DataFrame({
                    "message_id": ids,
                    "__source_file": infile.name,
                    "__row_in_chunk": np.arange(len(ids), dtype=np.int32),
                    "__text_nchars": [len(t) for t in texts],
                    "__emb_dim": emb.shape[1],
                    "__emb_dtype": str(EMB_DTYPE),
                    "__batch_used": used_bs
                })
                meta.to_csv(meta_path, index=False, compression="gzip")

                nrows = len(ids)
                rows_total_infile += nrows
                kept_rows += nrows

                # Housekeeping
                del df, texts, ids, emb, meta
                gc.collect()
                if torch.cuda.is_available():
                    torch.cuda.empty_cache()

                chunk_id += 1

            # Mark DONE (per input file)
            safe_write_text(dm, json.dumps({
                "infile": str(infile),
                "gpu_id": gpu_id,
                "chunks_written": chunk_id,
                "rows_embedded": rows_total_infile,
                "finished_utc": datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S")
            }))

            kept_files += 1
            dt = time.time() - t0
            log(f"✅ [GPU {gpu_id}] DONE {infile.name} | rows={rows_total_infile:,} | chunks={chunk_id:,} | {dt:,.1f}s")

        except Exception as e:
            # fail-soft: log and continue
            err_path = LOG_DIR / f"FAILED_gpu{gpu_id}.log"
            with open(err_path, "a") as f:
                f.write(f"{datetime.utcnow().isoformat()} | {infile} | {repr(e)}\n")
            log(f"⚠️ [GPU {gpu_id}] FAILED {infile.name}: {repr(e)}")
            continue

    log(f"🏁 [GPU {gpu_id}] Worker finished | files={kept_files:,} | rows={kept_rows:,}")
    return kept_files, kept_rows

# =============================================================================
# SHARDING FILES ACROSS GPUS
# =============================================================================

def shard_files(files, n_shards: int):
    shards = [[] for _ in range(n_shards)]
    for i, f in enumerate(files):
        shards[i % n_shards].append(f)
    return shards

