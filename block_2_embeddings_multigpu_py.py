# Model
EMBED_MODEL_NAME = "sentence-transformers/paraphrase-multilingual-mpnet-base-v2"
MAX_SEQ_LEN = 256

# Performance knobs
DEFAULT_BATCH_SIZE = 2048         # will auto-shrink on OOM
MAX_ROWS_PER_CHUNK = 200_000      # streaming inside a file (keeps RAM stable)
WRITE_EVERY_ROWS   = 200_000      # write embeddings per chunk

# Numeric format
EMB_DTYPE = np.float16            # smaller + fast

# GPU config
