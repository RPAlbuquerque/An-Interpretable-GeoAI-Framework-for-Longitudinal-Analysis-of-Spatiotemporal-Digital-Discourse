def main():
    print("\n>>> BLOCK 2 SCRIPT STARTED <<<", flush=True)
    print("Python executable:", sys.executable, flush=True)
    print("PID:", os.getpid(), flush=True)

    files = sorted(INPUT_ROOT.glob("*.csv.gz"))
    if not files:
        raise RuntimeError(f"❌ No input files found in {INPUT_ROOT}")

    log("=" * 110)
    log("🚀 BLOCK 2 — MULTI-GPU EMBEDDINGS (RESUMABLE)")
    log(f"Project root : {PROJECT_ROOT}")
    log(f"Input root   : {INPUT_ROOT}")
    log(f"Output root  : {OUT_ROOT}")
    log(f"Files found  : {len(files):,}")
    log(f"GPUs         : {GPU_IDS}")
    log(f"CPU threads  : {CPU_THREADS}")
    log(f"Model        : {EMBED_MODEL_NAME}")
    log(f"Batch        : {DEFAULT_BATCH_SIZE} (auto-shrink on OOM)")
    log(f"Chunk rows   : {MAX_ROWS_PER_CHUNK:,}")
    log("=" * 110)

    # Shard input files across GPUs
    shards = shard_files(files, len(GPU_IDS))

    ctx = get_context("spawn")  # CUDA-safe
    procs = []
    pipes = []

    # We use spawn processes via ctx.Process
    results_path = OUT_ROOT / "run_summary.json"

    # Start one process per GPU
    for gpu_id, shard in zip(GPU_IDS, shards):
        p = ctx.Process(target=worker_main, args=(gpu_id, shard), daemon=False)
        p.start()
        procs.append(p)
        log(f"🚀 Launched worker for GPU {gpu_id} | files={len(shard):,}")

    # Wait
    for p in procs:
        p.join()

    log("✅ BLOCK 2 FINISHED (all GPU workers joined)")
    log(f"Outputs: {OUT_ROOT}")
    log("=" * 110)

if __name__ == "__main__":
    main()