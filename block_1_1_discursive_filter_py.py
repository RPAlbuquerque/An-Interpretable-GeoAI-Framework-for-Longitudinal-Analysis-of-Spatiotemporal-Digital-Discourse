def process_one_file(infile_str: str) -> dict:
    infile = Path(infile_str)
    dm = done_marker_for(infile)
    outpath = out_path_for(infile)

    if dm.exists() and outpath.exists():
        return {"file": infile_str, "status": "SKIPPED", "kept": 0, "read": 0}

    kept_total = 0
    read_total = 0

    try:
        reader = pd.read_csv(
            infile,
            compression="gzip",
            dtype=str,              # preserve schema; no dtype inference surprises
            chunksize=CHUNKSIZE,
            low_memory=False
        )

        out_written = False

        for chunk_idx, df in enumerate(reader):
            if df is None or df.empty:
                continue

            read_total += len(df)

            mask = discursive_mask(df)
            if mask.any():
                out_df = df.loc[mask].copy()   # keep ALL columns
                kept = len(out_df)
                kept_total += kept

                # Append per chunk into one output file
                # (write header only once)
                if not out_written:
                    atomic_write_csv_gz(out_df, outpath)
                    out_written = True
                else:
                    # append safely: write temp then concatenate by appending text
                    # gzip append is supported; pandas doesn't do append to gzip cleanly,
                    # so we do a controlled append via text mode.
                    # Strategy: write chunk to tmp gz and then append decompressed lines (excluding header) to main gz.
                    tmp = outpath.with_suffix(outpath.suffix + f".chunk{chunk_idx:05d}.tmp.gz")
                    out_df.to_csv(tmp, index=False, compression="gzip")

                    # Append lines excluding header
                    import gzip as _gzip
                    with _gzip.open(outpath, "ab") as dst, _gzip.open(tmp, "rb") as src:
                        # skip header line in src
                        _ = src.readline()
                        shutil.copyfileobj(src, dst)
                    tmp.unlink(missing_ok=True)

                del out_df
                gc.collect()

            del df
            gc.collect()

        safe_json_write(dm, {
            "file": infile_str,
            "finished_utc": datetime.utcnow().strftime("%Y-%m-%d %H:%M:%S"),
            "read_rows": read_total,
            "kept_rows": kept_total,
            "output": str(outpath)
        })

        return {"file": infile_str, "status": "OK", "kept": kept_total, "read": read_total}

    except Exception as e:
        err_path = LOG_DIR / "failed_files.log"
        with open(err_path, "a") as f:
            f.write(f"{datetime.utcnow().isoformat()} | {infile_str} | {repr(e)}\n")
        return {"file": infile_str, "status": "FAILED", "kept": kept_total, "read": read_total}

