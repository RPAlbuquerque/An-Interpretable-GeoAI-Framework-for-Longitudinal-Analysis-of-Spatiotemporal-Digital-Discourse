def discursive_mask(df: pd.DataFrame) -> pd.Series:
    """
    Returns a boolean mask selecting discursive, stance-bearing, semantically rich tweets.
    Preserves all columns: this function filters rows only.
    """
    if "text" not in df.columns:
        return pd.Series([False] * len(df), index=df.index)

    text = df["text"].astype("string").fillna("").str.strip()

    # 1) Basic length / words
    m_len   = text.str.len().fillna(0) >= MIN_CHARS
    n_words = text.str.split().str.len().fillna(0)
    m_words = n_words >= MIN_WORDS

    # 2) Remove URL-only
    m_not_url_only = ~text.str.match(URL_ONLY_RE, na=False)

    # 3) Remove RTs / replies (very high noise for macrotheme discourse)
    m_not_rt    = ~text.str.match(RT_PREFIX_RE, na=False)
    m_not_reply = ~text.str.match(REPLY_PREFIX_RE, na=False)

    # 4) Must contain letters (drop emoji-only / punctuation-only)
    m_has_letter = text.str.match(HAS_LETTER_RE, na=False)

    # 5) Drop excessive repetitions
    m_no_repeat = ~text.str.contains(REPEAT_RE, na=False)

    # 6) Letter density constraints (proxy for semantic content)
    #    alpha_count via regex findall count (vectorized count)
    alpha_count = text.str.count(LETTER_RE)
    total_len   = text.str.len().clip(lower=1)
    alpha_ratio = (alpha_count / total_len)

    m_alpha_min   = alpha_count >= MIN_ALPHA_CHARS
    m_alpha_ratio = alpha_ratio >= MIN_ALPHA_RATIO

    # 7) Drop digit-heavy strings (often IDs, spam, scoreboard, etc.)
    digit_count = text.str.count(r"\d")
    digit_ratio = (digit_count / total_len)
    m_digit_ok  = digit_ratio <= MAX_DIGIT_RATIO

    # 8) Spammy hashtag/mention bursts
    hashtag_count  = text.str.count(r"#")
    mention_count  = text.str.count(r"@")
    m_hash_ok      = hashtag_count <= MAX_HASHTAGS
    m_mention_ok   = mention_count <= MAX_MENTIONS

    # Optional: enforce Portuguese language if column exists
    m_lang = pd.Series([True] * len(df), index=df.index)
    if ENFORCE_PT_LANG:
        lang_col = None
        for c in ["tweet_lang", "user_lang", "lang", "text_lang"]:
            if c in df.columns:
                lang_col = c
                break
        if lang_col is not None:
            lang = df[lang_col].astype("string").fillna("").str.lower().str.strip()
            # Accept common PT variants
            m_lang = lang.isin(["pt", "pt_br", "pt-br", "por", "portuguese", "português", "ptbr"]) | lang.str.startswith("pt")

    # Optional: enforce Brazil location (only if column exists)
    m_loc = pd.Series([True] * len(df), index=df.index)
    if ENFORCE_BR_LOCATION:
        loc_col = None
        for c in ["place", "user_location", "location"]:
            if c in df.columns:
                loc_col = c
                break
        if loc_col is not None:
            loc = df[loc_col].astype("string").fillna("").str.lower()
            # permissive contains to avoid false negatives
            m_loc = loc.str.contains("brasil|brazil", regex=True, na=False)

    return (
        m_len
        & m_words
        & m_not_url_only
        & m_not_rt
        & m_not_reply
        & m_has_letter
        & m_no_repeat
        & m_alpha_min
        & m_alpha_ratio
        & m_digit_ok
        & m_hash_ok
        & m_mention_ok
        & m_lang
        & m_loc
    )

