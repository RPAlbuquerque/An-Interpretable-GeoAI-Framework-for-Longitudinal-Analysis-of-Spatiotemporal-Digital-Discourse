# If a string has at least one letter => not emoji/punct-only
HAS_LETTER_RE = re.compile(r".*[A-Za-zÀ-ÖØ-öø-ÿ].*")

# Emoji-only / punct-only proxy: no letters at all
NO_LETTER_RE = re.compile(r"^[^A-Za-zÀ-ÖØ-öø-ÿ]+$")

